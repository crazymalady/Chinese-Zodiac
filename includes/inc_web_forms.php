<p> <h2>Web Forms and User Input</h2>
User input for Web pages is done through web forms. Web forms involve two components. The first is the actual web form with fields for the user to enter data. 
The second is a form processor, which reads the entered data and responds appropriately.

User input for Web pages can also be done through links using the <a> tag. web browsers automatically process user input when a link is clicked, and send a request to the Web server.
 
